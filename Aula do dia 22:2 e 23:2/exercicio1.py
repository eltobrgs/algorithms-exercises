# Crie um script que leia dois números e tente mostrar a soma entre eles

n1 = int(input('Digite um número qualquer:'))  # Solicita ao usuário para digitar o primeiro número
n2 = int(input('Digite outro número qualquer:'))  # Solicita ao usuário para digitar o segundo número
nf = n1 + n2  # Realiza a soma dos dois números e armazena o resultado na variável nf
print(f'O resultado da soma entre {n1} e {n2} é {nf}')  # Imprime o resultado da soma na tela