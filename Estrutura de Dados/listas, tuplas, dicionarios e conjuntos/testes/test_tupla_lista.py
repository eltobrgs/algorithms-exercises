# Exemplo de lista
frutas = ['maçã', 'banana', 'laranja']
frutas.append('uva')  # Adiciona 'uva' ao final da lista
frutas.remove('banana')  # Remove 'banana' da lista
print(frutas)  # Saída: ['maçã', 'laranja', 'uva']

# Exemplo de tupla
cores = ('vermelho', 'verde', 'azul')
# cores.append('amarelo')  # Erro! Tuplas são imutáveis e não podem ser modificadas
# cores.remove('verde')  # Erro! Tuplas não possuem o método 'remove'
print(cores)  # Saída: ('vermelho', 'verde', 'azul')

