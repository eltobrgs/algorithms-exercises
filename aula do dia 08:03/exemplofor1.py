# Utilizando um loop 'for' para iterar sobre uma sequência de números de 0 a 99
# O valor 100 não está incluso na sequência
for i in range(100):
    # Imprime o valor atual de 'i' durante cada iteração do loop
    print(i)

#Mas e se eu quiser um numero ate 100? Utilizando um loop 'for' para iterar sobre uma sequência de números de 0 a 100 
for i in range(100 + 1):
    # Imprime o valor atual de 'i' durante cada iteração do loop
    print(i)
